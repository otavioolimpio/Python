saldo = 100
deposito = 50

saldo += deposito  # Equivalente a: saldo = saldo + deposito
print(saldo)  # Saída: 150
saldo -= 20  # Equivalente a: saldo = saldo - 20
print(saldo)  # Saída: 130
saldo *= 2  # Equivalente a: saldo = saldo * 2  
print(saldo)  # Saída: 260
saldo /= 4  # Equivalente a: saldo = saldo / 4  
print(saldo)  # Saída: 65.0
saldo %= 7  # Equivalente a: saldo = saldo % 7
print(saldo)  # Saída: 2.0  
saldo **= 3  # Equivalente a: saldo = saldo ** 3
print(saldo)  # Saída: 8.0
saldo //= 2  # Equivalente a: saldo = saldo // 2
print(saldo)  # Saída: 4.0