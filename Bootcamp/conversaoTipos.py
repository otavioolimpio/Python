print(int("123"))        # Converte string para inteiro 
print(float("123.45"))   # Converte string para float
print(str(123))         # Converte inteiro para string
print(str(123.45))      # Converte float para string
print(bool(1))          # Converte inteiro para booleano (True)
print(bool(0))          # Converte inteiro para booleano (False)
print(bool(""))         # Converte string vazia para booleano (False)
print(bool("Hello"))    # Converte string não vazia para booleano (True)
print(list((1, 2, 3)))  # Converte tupla para lista
print(tuple([1, 2, 3])) # Converte lista para tupla

print(type(123))        # Verifica o tipo do valor (int)
print(type(123.45))     # Verifica o tipo do valor (float)
print(type("Hello"))    # Verifica o tipo do valor (str)
print(type([1, 2, 3]))  # Verifica o tipo do valor (list)
print(type((1, 2, 3)))  # Verifica o tipo do valor (tuple)

print(100/3)       # Divisão normal (float)
print(100//3)      # Divisão inteira (int)
