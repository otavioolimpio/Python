saldo = 1000
saque = 200
novo_saldo = saldo - saque
print("Saldo inicial:", saldo)
print("Saque realizado:", saque)

novo_saldo = saldo + 100
print("Novo saldo após depósito:", novo_saldo)

novo_saldo = novo_saldo * 1.05
print("Saldo após rendimento de 5%:", novo_saldo)   

novo_saldo = novo_saldo / 2
print("Saldo após divisão em duas partes:", novo_saldo)

novo_saldo = novo_saldo % 3
print("Resto da divisão do saldo por 3:", novo_saldo)

novo_saldo = novo_saldo ** 2
print("Saldo elevado ao quadrado:", novo_saldo)

novo_saldo = novo_saldo // 4
print("Saldo dividido por 4 (divisão inteira):", novo_saldo)

print("Operações aritméticas concluídas.")