saldo = 200
saques = 30

print(saldo == saques)  # Verifica se saldo é igual a saques
print(saldo != saques)  # Verifica se saldo é diferente de saques
print(saldo > saques)   # Verifica se saldo é maior que saques
print(saldo < saques)   # Verifica se saldo é menor que saques  
print(saldo >= saques)  # Verifica se saldo é maior ou igual a saques
print(saldo <= saques)  # Verifica se saldo é menor ou igual a saques
print(saldo == 200)     # Verifica se saldo é igual a 200
print(saques != 50)     # Verifica se saques é diferente de 50